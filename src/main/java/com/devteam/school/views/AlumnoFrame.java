    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        Alumno alumno = new Alumno();
        alumno.setNombre(jtfNombre.getText());
        alumno.setApellidos(jtfApellidos.getText());
        alumno.setTelefono(jtfTelefono.getText());
        alumno.setDireccion(jtfDireccion.getText());
        alumno.setEmail(jtfEmail.getText());
        alumno.setFechaNacimiento(jdcFechaNacimiento.getDate());

        if (accion.equals("guardar")) {
            alumno = alumnoController.saveAlumno(alumno);
            if (alumno != null ) {
                JOptionPane.showMessageDialog(rootPane, "El alumno fue registrado correctamente");
                alumnos = alumnoController.getAlumnos();
                fillTable(alumnos);
                jtfId.setText( String.valueOf(alumno.getId()) );
                inhabilitar(); 
                
            }
        }
        else if (accion.equals("editar")){
            alumno.setId(Integer.parseInt(jtfId.getText()));

            alumno = alumnoController.updateAlumno(alumno);
            if (alumno != null ) {
                JOptionPane.showMessageDialog(rootPane, "El Alumno fue actualizado correctamente");
                alumnos = alumnoController.getAlumnos();
                fillTable(alumnos);
                //inhabilitar();
            }
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

